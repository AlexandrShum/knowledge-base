// Array of strings in JS
const array = ["Corey Taylor", "Mick Thomson", "Paul Gray"];

console.log(array); // This command will show our array to terminal ["Corey Taylor", "Mick Thomson", "Paul Gray"]

// We can find element using index
const secondElement = array[1];
console.log(secondElement); // Will show in console second value of array -> "Mick Thomson"

// For adding element to end of list we should use push method
array.push("Joey Jordison");
console.log(array); // Now we will see 4 elements in our array -> ["Corey Taylor", "Mick Thomson", "Paul Gray", "Joey Jordison"]
